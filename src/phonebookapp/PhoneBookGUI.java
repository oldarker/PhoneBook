    private void loadTableData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        List<Contact> contacts = dc.getContact();
        
        for (Contact c : contacts) {
            model.addRow(new Object[]{c.getName(), c.getPhone()});
        }
    }
    
