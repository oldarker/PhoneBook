    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String oldName = jTextField3.getText();
        
        if (!dc.contactExists(oldName))
        {
            JOptionPane.showMessageDialog(this,
            "Такого имени нет",
            "Ошибка ввода",
            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        String newName = jTextField4.getText();
        if (dc.isValidName(newName) == false)
        {
            JOptionPane.showMessageDialog(this,
            "Имя должно начинаться с буквы и содержать только буквы, пробелы или дефис!",
            "Ошибка ввода",
            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        dc.update(oldName, newName);
        loadTableData();
        jTextField3.setText("");
        jTextField4.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        if (dc.isValidName(name) == false)
        {
            JOptionPane.showMessageDialog(this,
            "Имя должно начинаться с буквы и содержать только буквы, пробелы или дефис!",
            "Ошибка ввода",
            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        if (dc.contactExists(name))
        {
            JOptionPane.showMessageDialog(this,
            "Такое имя уже существует",
            "Ошибка ввода",
            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        String number = jTextField2.getText();
        if (!dc.isValidPhone(number)) {
        JOptionPane.showMessageDialog(this,
            "Телефон должен содержать ровно 11 цифр (без пробелов и символов)!",
            "Ошибка ввода",
            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        dc.add(name, number);
        loadTableData();
        jTextField1.setText("");
        jTextField2.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

